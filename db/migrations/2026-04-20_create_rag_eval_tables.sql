CREATE TABLE IF NOT EXISTS rag_eval_dataset (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT 'RAG评测集ID',
  user_id BIGINT NOT NULL COMMENT '用户ID',
  material_id BIGINT NOT NULL COMMENT '默认关联资料ID',
  name VARCHAR(120) NOT NULL COMMENT '评测集名称',
  description VARCHAR(500) DEFAULT NULL COMMENT '评测集说明',
  status VARCHAR(20) NOT NULL DEFAULT 'ACTIVE' COMMENT '状态: ACTIVE/ARCHIVED',
  sample_count INT NOT NULL DEFAULT 0 COMMENT '样本数量',
  last_run_id BIGINT DEFAULT NULL COMMENT '最近一次运行ID',
  last_run_at DATETIME DEFAULT NULL COMMENT '最近一次运行时间',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  CONSTRAINT fk_rag_eval_dataset_user FOREIGN KEY (user_id) REFERENCES sys_user (id),
  CONSTRAINT fk_rag_eval_dataset_material FOREIGN KEY (material_id) REFERENCES study_material (id),
  KEY idx_rag_eval_dataset_user_created (user_id, created_at),
  KEY idx_rag_eval_dataset_material (material_id)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COMMENT = 'RAG检索评测集表';

CREATE TABLE IF NOT EXISTS rag_eval_sample (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT 'RAG评测样本ID',
  dataset_id BIGINT NOT NULL COMMENT '评测集ID',
  user_id BIGINT NOT NULL COMMENT '用户ID',
  material_id BIGINT NOT NULL COMMENT '关联资料ID',
  query_text VARCHAR(500) NOT NULL COMMENT '检索问题',
  expected_segment_ids TEXT DEFAULT NULL COMMENT '标准相关分段ID列表(JSON)',
  expected_page_nos TEXT DEFAULT NULL COMMENT '标准相关页码列表(JSON)',
  expected_keywords VARCHAR(500) DEFAULT NULL COMMENT '期望命中关键词',
  tag VARCHAR(80) DEFAULT NULL COMMENT '样本标签，如定义/流程/区别/实验',
  difficulty TINYINT DEFAULT 3 COMMENT '样本难度: 1-5',
  source_type VARCHAR(30) NOT NULL DEFAULT 'HUMAN' COMMENT '来源: HUMAN/AI_ASSISTED/IMPORTED',
  note VARCHAR(500) DEFAULT NULL COMMENT '标注备注',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  CONSTRAINT fk_rag_eval_sample_dataset FOREIGN KEY (dataset_id) REFERENCES rag_eval_dataset (id),
  CONSTRAINT fk_rag_eval_sample_user FOREIGN KEY (user_id) REFERENCES sys_user (id),
  CONSTRAINT fk_rag_eval_sample_material FOREIGN KEY (material_id) REFERENCES study_material (id),
  KEY idx_rag_eval_sample_dataset (dataset_id),
  KEY idx_rag_eval_sample_material (material_id),
  KEY idx_rag_eval_sample_user_created (user_id, created_at)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COMMENT = 'RAG检索评测样本表';

CREATE TABLE IF NOT EXISTS rag_eval_run (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT 'RAG评测运行ID',
  dataset_id BIGINT NOT NULL COMMENT '评测集ID',
  user_id BIGINT NOT NULL COMMENT '用户ID',
  material_id BIGINT NOT NULL COMMENT '默认资料ID',
  status VARCHAR(20) NOT NULL DEFAULT 'RUNNING' COMMENT '状态: RUNNING/SUCCESS/PARTIAL_FAILED/FAILED',
  retrieval_limit INT NOT NULL DEFAULT 5 COMMENT '每条问题检索返回数量',
  total_samples INT NOT NULL DEFAULT 0 COMMENT '总样本数',
  evaluated_samples INT NOT NULL DEFAULT 0 COMMENT '成功评测样本数',
  failed_samples INT NOT NULL DEFAULT 0 COMMENT '失败样本数',
  hit_at_1 DOUBLE DEFAULT 0 COMMENT 'Hit@1',
  hit_at_3 DOUBLE DEFAULT 0 COMMENT 'Hit@3',
  hit_at_5 DOUBLE DEFAULT 0 COMMENT 'Hit@5',
  recall_at_1 DOUBLE DEFAULT 0 COMMENT 'Recall@1',
  recall_at_3 DOUBLE DEFAULT 0 COMMENT 'Recall@3',
  recall_at_5 DOUBLE DEFAULT 0 COMMENT 'Recall@5',
  mrr DOUBLE DEFAULT 0 COMMENT 'Mean Reciprocal Rank',
  avg_latency_ms DOUBLE DEFAULT 0 COMMENT '平均耗时(ms)',
  error_message TEXT DEFAULT NULL COMMENT '运行错误摘要',
  started_at DATETIME DEFAULT NULL COMMENT '开始时间',
  finished_at DATETIME DEFAULT NULL COMMENT '结束时间',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  CONSTRAINT fk_rag_eval_run_dataset FOREIGN KEY (dataset_id) REFERENCES rag_eval_dataset (id),
  CONSTRAINT fk_rag_eval_run_user FOREIGN KEY (user_id) REFERENCES sys_user (id),
  CONSTRAINT fk_rag_eval_run_material FOREIGN KEY (material_id) REFERENCES study_material (id),
  KEY idx_rag_eval_run_dataset_created (dataset_id, created_at),
  KEY idx_rag_eval_run_user_created (user_id, created_at)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COMMENT = 'RAG检索评测运行表';

CREATE TABLE IF NOT EXISTS rag_eval_run_item (
  id BIGINT PRIMARY KEY AUTO_INCREMENT COMMENT 'RAG评测运行明细ID',
  run_id BIGINT NOT NULL COMMENT '评测运行ID',
  dataset_id BIGINT NOT NULL COMMENT '评测集ID',
  sample_id BIGINT NOT NULL COMMENT '评测样本ID',
  user_id BIGINT NOT NULL COMMENT '用户ID',
  material_id BIGINT NOT NULL COMMENT '关联资料ID',
  query_text VARCHAR(500) NOT NULL COMMENT '检索问题快照',
  expected_segment_ids TEXT DEFAULT NULL COMMENT '标准相关分段ID列表(JSON)',
  expected_page_nos TEXT DEFAULT NULL COMMENT '标准相关页码列表(JSON)',
  retrieved_segment_ids TEXT DEFAULT NULL COMMENT '实际召回分段ID列表(JSON)',
  retrieved_page_nos TEXT DEFAULT NULL COMMENT '实际召回页码列表(JSON)',
  hit_rank INT DEFAULT NULL COMMENT '首次命中的排名',
  reciprocal_rank DOUBLE DEFAULT 0 COMMENT '倒数排名',
  recall_at_1 DOUBLE DEFAULT 0 COMMENT 'Recall@1',
  recall_at_3 DOUBLE DEFAULT 0 COMMENT 'Recall@3',
  recall_at_5 DOUBLE DEFAULT 0 COMMENT 'Recall@5',
  latency_ms BIGINT DEFAULT NULL COMMENT '本条耗时(ms)',
  error_message TEXT DEFAULT NULL COMMENT '本条错误信息',
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  CONSTRAINT fk_rag_eval_run_item_run FOREIGN KEY (run_id) REFERENCES rag_eval_run (id),
  CONSTRAINT fk_rag_eval_run_item_dataset FOREIGN KEY (dataset_id) REFERENCES rag_eval_dataset (id),
  CONSTRAINT fk_rag_eval_run_item_sample FOREIGN KEY (sample_id) REFERENCES rag_eval_sample (id),
  CONSTRAINT fk_rag_eval_run_item_user FOREIGN KEY (user_id) REFERENCES sys_user (id),
  CONSTRAINT fk_rag_eval_run_item_material FOREIGN KEY (material_id) REFERENCES study_material (id),
  KEY idx_rag_eval_run_item_run (run_id),
  KEY idx_rag_eval_run_item_sample (sample_id)
) ENGINE = InnoDB DEFAULT CHARSET = utf8mb4 COMMENT = 'RAG检索评测运行明细表';
